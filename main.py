def type(_, msg):
    origText = msg.text.split("@type ", maxsplit=1)[1]
    text = origText
    tbp = "" # to be printed
    symbol = "▒"
 
    while(tbp != origText):
        msg.edit(tbp + symbol)
        sleep(0.05) # 50 ms

        tbp = tbp + text[0]
        text = text[1:]

        msg.edit(tbp)
        sleep(0.05)


